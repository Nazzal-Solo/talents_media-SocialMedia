async function populateLocations() {
  try {
    await testConnection();
    console.log('üîÑ Starting location population...');

    // Clear existing data
    console.log('Clearing existing location data...');
    await query('DELETE FROM apply_cities');
    await query('DELETE FROM apply_countries');

    // Insert countries
    console.log(`Inserting ${countries.length} countries...`);
    const countryMap = new Map<string, string>(); // iso2 -> id

    for (const country of countries) {
      const normalizedKey = normalizeText(country.name);
      const result = await query<{ id: string }>(
        `INSERT INTO apply_countries (name, iso2, iso3, aliases, normalized_key)
         VALUES ($1, $2, $3, $4, $5)
         RETURNING id`,
        [
          country.name,
          country.iso2,
          country.iso3,
          country.aliases,
          normalizedKey,
        ]
      );
      countryMap.set(country.iso2, result.rows[0].id);
    }

    console.log(`‚úÖ Inserted ${countries.length} countries`);

    // Insert cities
    console.log(`Inserting ${cities.length} cities...`);
    let inserted = 0;
    let skipped = 0;

    for (const city of cities) {
      const countryId = countryMap.get(city.countryIso2);
      if (!countryId) {
        console.warn(
          `Skipping city ${city.name} - country ${city.countryIso2} not found`
        );
        skipped++;
        continue;
      }

      const normalizedKey = normalizeText(city.name);
      await query(
        `INSERT INTO apply_cities (name, country_id, admin_region, population, latitude, longitude, aliases, normalized_key)
         VALUES ($1, $2, $3, $4, $5, $6, $7, $8)
         ON CONFLICT (normalized_key, country_id) DO NOTHING`,
        [
          city.name,
          countryId,
          city.adminRegion || null,
          city.population || null,
          city.latitude || null,
          city.longitude || null,
          city.aliases || [],
          normalizedKey,
        ]
      );
      inserted++;
    }

    console.log(`‚úÖ Inserted ${inserted} cities (${skipped} skipped)`);
    console.log('‚úÖ Location population completed successfully');
  } catch (error) {
    console.error('‚ùå Location population failed:', error);
    throw error;
  }
}

// Run if called directly
if (require.main === module) {
  populateLocations()
    .then(() => {
      console.log('Done');
      process.exit(0);
    })
    .catch(error => {
      console.error('Error:', error);
      process.exit(1);
    });
}

export { populateLocations };
